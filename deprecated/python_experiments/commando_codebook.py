# 6502 assembly template
# Zero-page layout:
#   $70/$71 = V1 stream ptr
#   $72/$73 = V2 stream ptr
#   $74/$75 = V3 stream ptr
#   $76/$77 = FV stream ptr
#   $78     = saved ID
#   $7B/$7C = scratch 16-bit ptr (codebook entry addr)
#
# Codebook lookup for 7-byte entries:
#   offset = ID * 7 = ID * 8 - ID  (using 3x ASL then subtract)
#   ptr = codebook_vN + offset
#
# Codebook lookup for 4-byte entries:
#   offset = ID * 4  (2x ASL, 16-bit)
#   ptr = codebook_fv + offset

