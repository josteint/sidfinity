def generate_asm(cb_v1, cb_v2, cb_v3, cb_fv, s_v1, s_v2, s_v3, s_fv, nframes):
    sv1, sv2, sv3, sfv = len(s_v1), len(s_v2), len(s_v3), len(s_fv)
    cbv  = CODEBOOK_SIZE * 7 * 3
    cbfv = CODEBOOK_SIZE * 4
    total = sv1 + sv2 + sv3 + sfv + cbv + cbfv
    orig  = nframes * 25

    header = PLAYER_TEMPLATE.format(
        nframes=nframes, cb_size=CODEBOOK_SIZE,
        sv1=sv1, sv2=sv2, sv3=sv3, sfv=sfv,
        cbv=cbv, cbfv=cbfv, total=total, orig=orig,
        pct=total * 100 // orig,
    )

    parts = [header]
    parts.append('; ===== CODEBOOKS =====')
    parts.append(asm_codebook(cb_v1, 'codebook_v1', 7))
    parts.append(asm_codebook(cb_v2, 'codebook_v2', 7))
    parts.append(asm_codebook(cb_v3, 'codebook_v3', 7))
    parts.append(asm_codebook(cb_fv, 'codebook_fv', 4))
    parts.append('')
    parts.append('; ===== STREAMS =====')
    parts.append(asm_stream(s_v1, 'stream_v1'))
    parts.append(asm_stream(s_v2, 'stream_v2'))
    parts.append(asm_stream(s_v3, 'stream_v3'))
    parts.append(asm_stream(s_fv, 'stream_fv'))
    parts.append('')
    return '\n'.join(parts)


def main():
    print("Loading frames...")
    frames = load_frames(FRAMES_CSV)
    print(f"  {len(frames)} frames loaded")

    v1_tuples = [tuple(f[0:7])   for f in frames]
    v2_tuples = [tuple(f[7:14])  for f in frames]
    v3_tuples = [tuple(f[14:21]) for f in frames]
    fv_tuples = [tuple(f[21:25]) for f in frames]

    print("Building codebooks...")
    cb_v1 = build_codebook(v1_tuples)
    cb_v2 = build_codebook(v2_tuples)
    cb_v3 = build_codebook(v3_tuples)
    cb_fv = build_codebook(fv_tuples)

    print(f"  V1: {len(cb_v1)} entries (top freq={Counter(v1_tuples).most_common(1)[0][1]})")
    print(f"  V2: {len(cb_v2)} entries")
    print(f"  V3: {len(cb_v3)} entries")
    print(f"  FV: {len(cb_fv)} entries")

    print("Encoding streams...")
    s_v1, il1 = encode_stream(v1_tuples, cb_v1)
    s_v2, il2 = encode_stream(v2_tuples, cb_v2)
    s_v3, il3 = encode_stream(v3_tuples, cb_v3)
    s_fv, il4 = encode_stream(fv_tuples, cb_fv)

    print(f"  V1: {len(s_v1)}B  {il1} inlines")
    print(f"  V2: {len(s_v2)}B  {il2} inlines")
    print(f"  V3: {len(s_v3)}B  {il3} inlines")
    print(f"  FV: {len(s_fv)}B  {il4} inlines")

    orig  = len(frames) * 25
    total = (len(s_v1) + len(s_v2) + len(s_v3) + len(s_fv)
             + CODEBOOK_SIZE * 7 * 3 + CODEBOOK_SIZE * 4)
    print(f"\nCompression: {orig} -> {total} bytes ({total*100//orig}%)")

    print("\nGenerating assembly...")
    asm = generate_asm(cb_v1, cb_v2, cb_v3, cb_fv, s_v1, s_v2, s_v3, s_fv, len(frames))

    os.makedirs(os.path.dirname(OUT_ASM), exist_ok=True)
    with open(OUT_ASM, 'w') as f:
        f.write(asm)
    print(f"  Written: {OUT_ASM}")

    return cb_v1, cb_v2, cb_v3, cb_fv, s_v1, s_v2, s_v3, s_fv, frames


def build_psid(prg_path, out_path, load_addr=0x1000, init_addr=0x1000, play_addr=0x1003):
    """Wrap a raw PRG in a PSID v2 header."""
    import struct

    with open(prg_path, 'rb') as f:
        prg_data = f.read()

    hdr = bytearray(124)
    hdr[0:4] = b'PSID'
    struct.pack_into('>H', hdr, 4, 2)
    struct.pack_into('>H', hdr, 6, 0x7C)
    struct.pack_into('>H', hdr, 8, 0x0000)   # load addr embedded in data
    struct.pack_into('>H', hdr, 10, init_addr)
    struct.pack_into('>H', hdr, 12, play_addr)
    struct.pack_into('>H', hdr, 14, 1)        # 1 song
    struct.pack_into('>H', hdr, 16, 1)        # default song 1
    struct.pack_into('>I', hdr, 18, 0)        # speed: VBI

    title    = b'Commando (codebook)'
    author   = b'Rob Hubbard'
    released = b'1985 Elite / SIDfinity codebook'
    hdr[22:22+min(32,len(title))]    = title[:32]
    hdr[54:54+min(32,len(author))]   = author[:32]
    hdr[86:86+min(32,len(released))] = released[:32]
    struct.pack_into('>H', hdr, 118, 0x0002)  # PAL flag

    prg_with_addr = struct.pack('<H', load_addr) + prg_data
    out = bytes(hdr) + prg_with_addr

    with open(out_path, 'wb') as f:
        f.write(out)
    return len(out)


