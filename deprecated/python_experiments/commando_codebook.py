#!/usr/bin/env python3
"""
Commando codebook player generator.

Approach:
- Capture 1500 frames of ground truth (25 regs per frame)
- For each voice (7 regs) + filter/vol (4 regs), find common 7-byte tuples
- Build a 240-entry codebook for each voice channel
- IDs 0..239 = codebook lookup
- ID 254 ($FE) = end of stream (loop)
- ID 255 ($FF) = inline (raw bytes follow)
- Per voice, store a stream of IDs

SID CSV column order per voice:
  cols 0-6:   V1: freq_lo, freq_hi, pw_lo, pw_hi, ctrl, ad, sr
  cols 7-13:  V2: ...
  cols 14-20: V3: ...
  cols 21-24: FILT: lo, hi, ctrl, mode_vol

SID register map (natural order = CSV order):
  V1: $D400=freq_lo, $D401=freq_hi, $D402=pw_lo, $D403=pw_hi, $D404=ctrl, $D405=ad, $D406=sr
  V2: $D407-$D40D (same pattern)
  V3: $D40E-$D414 (same pattern)
  Filter: $D415=filt_lo, $D416=filt_hi, $D417=filt_ctrl, $D418=mode_vol
"""

import sys
import os
from collections import Counter

FRAMES_CSV = '/tmp/commando_frames.csv'
OUT_ASM    = '/home/jtr/sidfinity/demo/hubbard/Commando_hg14.s'
OUT_SID    = '/home/jtr/sidfinity/demo/hubbard/Commando_hg14.sid'

CODEBOOK_SIZE = 240
INLINE_ID     = 0xFF  # signals raw bytes follow in stream
END_ID        = 0xFE  # signals end of stream (player loops)


# ---------------------------------------------------------------------------
def load_frames(path):
    frames = []
    with open(path) as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            vals = [int(x, 16) for x in line.split(',')]
            assert len(vals) == 25, f"Expected 25 columns, got {len(vals)}"
            frames.append(vals)
    return frames


def build_codebook(tuples_list, max_entries=CODEBOOK_SIZE):
    counter = Counter(tuples_list)
    top = [t for t, _ in counter.most_common(max_entries)]
    return top


def encode_stream(tuples_list, codebook):
    """
    Returns (stream_bytes, inline_count).
    stream_bytes: list of ints
      - single ID byte if tuple in codebook
      - INLINE_ID followed by raw tuple bytes if not in codebook
    Does NOT include END_ID - caller appends it.
    """
    cb_map = {t: i for i, t in enumerate(codebook)}
    stream = []
    inline_count = 0
    for t in tuples_list:
        if t in cb_map:
            stream.append(cb_map[t])
        else:
            stream.append(INLINE_ID)
            stream.extend(list(t))
            inline_count += 1
    stream.append(END_ID)
    return stream, inline_count


# ---------------------------------------------------------------------------
def asm_stream(data, label):
    lines = [f"{label}"]
    for i in range(0, len(data), 16):
        chunk = data[i:i + 16]
        hex_str = ','.join(f'${b:02X}' for b in chunk)
        lines.append(f"        .byte {hex_str}")
    return '\n'.join(lines)


def asm_codebook(cb, label, tsize):
    """Emit codebook as flat byte array (CODEBOOK_SIZE * tsize bytes)."""
    flat = []
    for t in cb:
        flat.extend(list(t))
    # Pad to CODEBOOK_SIZE entries
    while len(flat) < CODEBOOK_SIZE * tsize:
        flat.append(0)
    lines = [f"{label}"]
    for i in range(0, len(flat), 16):
        chunk = flat[i:i + 16]
        hex_str = ','.join(f'${b:02X}' for b in chunk)
        lines.append(f"        .byte {hex_str}")
    return '\n'.join(lines)


